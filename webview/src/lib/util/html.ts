// Tiny DOM helpers shared across pages. Kept framework-agnostic so the
// Library / Extract pages can use them too.

export function escapeHtml(s: string): string {
  return s
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

export function fmtBytes(n: number): string {
  if (n < 1024) return `${n} B`;
  if (n < 1024 * 1024) return `${(n / 1024).toFixed(1)} KB`;
  if (n < 1024 * 1024 * 1024) return `${(n / 1024 / 1024).toFixed(1)} MB`;
  return `${(n / 1024 / 1024 / 1024).toFixed(2)} GB`;
}

/** Format an integer as a 6-digit `#rrggbb` hex color (sans leading `#`). */
export function rgbHex(n: number): string {
  return (n & 0xffffff).toString(16).padStart(6, '0');
}
